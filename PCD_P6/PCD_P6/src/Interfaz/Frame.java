    public static void main(String args[]) {
        
        int ancho=1024;
        int alto =764;
        
        Frame f=new Frame();
        f.setTitle("Cooperativa");
        f.setSize(ancho, alto);
        f.setLocationRelativeTo(null);
        
        Canva cv = new Canva(ancho, alto);
        Generador gen = new Generador(cv);
        
        cv.setGenerador(gen);
        
        f.add(cv);
        f.setVisible(true);
        
        Random rand = new Random(System.nanoTime());
        int num_hilos = 20;
        Thread hilos[] = new Thread[num_hilos];
        for (int i = 0; i < num_hilos; i++) {
            int num_alea = rand.nextInt(10);
            if (num_alea < 7) {
                Camion c = new Camion(i+1, gen, cv);
                gen.addVehiculoEspera(c);

                hilos[i] = c;
            } else {
                Tractor tr = new Tractor(i+1, gen, cv);
                Thread t = new Thread(tr);
                gen.addVehiculoEspera(tr);

                hilos[i] = t;
            }
            hilos[i].start();
            try {
                Thread.sleep(rand.nextInt(1000) + 1000);
            } catch (InterruptedException ex) {
                System.getLogger(Frame.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        }

        for (int i = 0; i < num_hilos; i++) {
            try {
                hilos[i].join();
            } catch (InterruptedException ex) {
                System.getLogger(Frame.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        }
    }

   

