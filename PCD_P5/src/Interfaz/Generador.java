    public static void main(String args[]) throws InterruptedException {
        int alto = 600;
        int ancho = 900;

        Generador f = new Generador();

        f.setTitle("Parking");
        f.setSize(ancho, alto);
        
        f.setLocationRelativeTo(null);

        Canva cv = new Canva(ancho, alto);
        f.add(cv);
        Parking parking = new Parking();
        cv.setParking(parking);
        parking.setCanvas(cv);
        
        f.setVisible(true);

        
        Random rand = new Random(System.nanoTime());
        int num_hilos=30;
        
        Thread[] hilos = new Thread[num_hilos];
        for(int i=0; i<num_hilos;i++){
            int id=i+1;
            int num_alea= rand.nextInt(2);
            if(num_alea==0){
                Electrico e = new Electrico(id, parking);   
                hilos[i]=e;
                hilos[i].start();
            }else{
                Combustion com= new Combustion(id,parking);
                Thread c= new Thread(com);
                hilos[i]=c;
                hilos[i].start();
                
            }           
            Thread.sleep(rand.nextInt(1000)+1000);
        }
        
        for(int i=0; i<num_hilos; i++){
            hilos[i].join();
        }

    }


